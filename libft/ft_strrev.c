#include "libft.h"

char	*ft_strrev(char *str)
{
	char			c;
	unsigned int	i;
	unsigned int	len;

	len = ft_strlen((const char *)str);
	i = 0;
	while (i < len / 2)
	{
		c = str[i];
		str[i] = str[len - i - 1];
		str[len - i - 1] = c;
		i++;
	}
	return (str);
}
